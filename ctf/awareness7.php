                                <h6><br><br>Hackers can observe ascertain strategic features also known as reconnaissance inside a company’s network.</h6>
                                <h6 class="bold"><br><br>Suspicious network activity - Reconnaissance attacks (AKA Information Gathering)</h6>
                                
                                <li>Traffic hiding (VPNs, proxies, and Tor connections)</li>
                                <li>Internal network scans</li>
                                <li>Remote process execution attempts</li>
                                <li>Net session enumeration on hosts</li>
                                <li>Active directory enumeration</li>
                                <li>Parameter scans</li>
                                
                                <h6 class="bold"><br><br>Insecure protocols inside the network</h6>
                                <li>Sensitive data is transmitted in cleartext. This means that anyone on the network, including a potential attacker, can intercept traffic and search it for sensitive information such as usernames and passwords. Another problem often found, along with unsecured protocols, is dictionary passwords.</li>
                                
                                <h6 class="bold"><br><br>Insecure communication through network</h6>
                                <li>Visit questionable sites, download torrents, install chat programs, or use remote access utilities, these measures are there to maintain an acceptable security level, but quite often users ignore them.</li>
                                <li>This calls for a reminder of the consequences of poor "network hygiene.".</li>
                                <li>•	Pass sensitive unencrypted data over network</li>
                                <li>•	Use of remote access tools, bit torrent, weak passwords, chat programs</li>
                                <li>•	Use of NetBIOS</li>
                                <li>•	Open network ports of the parameter.</li>
                                
                                <h6 class="bold"><br><br>DOSS attacks-Denial of service attacks</h6>
                                <h6>Two major sources:</h6>
                                <li>When a maliciously formatted packet is forwarded to a host or application, and the receiver is unable to handle an unexpected condition causes the receiving device to crash.</li>
                                <li>When a network is unable to handle an enormous quantity of data, causing to crash.</li>
                                
                                <h6 class="bold"><br><br>DDOSS-Distributed Denial of service attack</h6>
                                <li>Similar in intent to a DOSS attack, except that DDOSS attack increase in magnitude because it originates from multiple, coordinated sources.</li>
                                
                                <h6 class="bold"><br><br>Don’ts</h6>
                                <li>Do not surf the internet or read suspicious emails while logged in</li>
                                <li>Do not run backups without verifying restores.</li>
                                <li>Do not run unverified downloads.</li>
                                <li>Do not perform changes without proper validation</li>
                                
                                <h6 class="bold"><br><br>Do’s</h6>
                                <li>Share information only on a "need to know" basis</li>
                                <li>Use different passwords for different tools and applications.</li>
                                <li>Use privileged accounts judiciously.</li>
                                <li>Be aware of latest patch release bulletins.</li>
                                <li>Lock your screen when stepping out from the work area.</li>
                                <li>Back up and disaster recovery</li>
                                <li>Protect the physical systems and network devices </li>
                                <li>Keep the network lean by disabling unnecessary services and limiting unnecessary communication.</li>
                                <li>Use physical separation of sensitive data and virtual separation of sensitive data</li>
                                <li>Manage secure access</li>
                                <li>Keep network devices up to date</li>
